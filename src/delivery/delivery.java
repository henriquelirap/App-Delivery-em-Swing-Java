    private void salvarlancheActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarlancheActionPerformed
        if (addBacon.isSelected() || addQueijo.isSelected()) {
            if (addBacon.isSelected() && lanche != null) {
                total += 4;
                adicional = " + (Adicional de Bacon R$4,00)";
            }
            if (addQueijo.isSelected() && lanche != null) {
                total += 4;
                adicional = " + (Adicional de Queijo R$4,00)";
            }
            if (addBacon.isSelected() && addQueijo.isSelected() && lanche != null) {
                adicional = " + (Adicional de Queijo e Bacon R$8,00)";
            }
        }

        if (lanche != null && precoLanche != null && adicional == null) {
            info.add("1 " + lanche + " - " + precoLanche + "\n");
            painel.setText(info.toString());
        }
        if (lanche != null && precoLanche != null && adicional != null) {
            info.add("1 " + lanche + " - " + precoLanche + adicional + "\n");
            painel.setText(info.toString());
        }

        if (csalada.isSelected()) {
            total += 20;
        }
        if (cbacon.isSelected()) {
            total += 22;
        }
        if (cegg.isSelected()) {
            total += 22;
        }
        if (cfrango.isSelected()) {
            total += 22;
        }
        if (cvegan.isSelected()) {
            total += 25;
        }
        if (ccheddar.isSelected()) {
            total += 24;
        }
        if (cbarbecue.isSelected()) {
            total += 25;
        }
        if (ctbone.isSelected()) {
            total += 28;
        }

        lanche = null;
        precoLanche = null;
        addBacon.setSelected(false);
        addQueijo.setSelected(false);
        csalada.setSelected(false);
        cbacon.setSelected(false);
        cegg.setSelected(false);
        cfrango.setSelected(false);
        cvegan.setSelected(false);
        ccheddar.setSelected(false);
        cbarbecue.setSelected(false);
        ctbone.setSelected(false);
        totalFinal = total;
        valPedido.setText(Double.toString(total));
        valTotal.setText(Double.toString(totalFinal));
        
    }//GEN-LAST:event_salvarlancheActionPerformed

