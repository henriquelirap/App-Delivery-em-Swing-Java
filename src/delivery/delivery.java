    private void confirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmarActionPerformed
        String complementoAux = complemento.getText();
  
        if(dinheiro.isSelected() || cartao.isSelected()){            
            if(dinheiro.isSelected()){
                pgto = "Dinheiro";
            }
            if(cartao.isSelected()){
                pgto = "Cartão";
            }      
            if(dinheiro.isSelected() && cartao.isSelected()){
                pgto = "Dinheiro e Cartão";
            }
        }
        
        if (complementoAux == null) {
            complementoAux = "";
        }

        String entrega = "Dados da Entrega: \n"
                + "Nome: " + nome.getText() + ", Contato: " + contato.getText() + "\n"
                + "Rua: " + rua.getText() + ", Número: " + numero.getText() + ", Bairro: " + bairro.getText() + ", Complemento: " + complementoAux + "\n"
                + "Cidade: " + cidade.getText() + ", Estado: " + estado.getText() + "\n"
                + "CEP: " + cep.getText() + "\n"
                + "Pedido: " + info.toString() + "\n"
                + tipoEntrega + "\n"
                + "Valor Pedido: R$" + total + "\n"
                + "Valor Total: R$" + totalFinal + "\n"
                + "Forma de Pagamento: " + pgto;

        byte[] buff = entrega.getBytes();

        try {
            RandomAccessFile raf = new RandomAccessFile("C:\\Users\\henri\\Desktop\\" + nome.getText() +".txt", "rw");           
            raf.write(buff);
            raf.close();
        } catch (IOException ex) {
            ex.printStackTrace();
        }
        dinheiro.setSelected(false);
        cartao.setSelected(false);
        delivery.setSelected(false);
        retirada.setSelected(false);
        nome.setText("");
        contato.setText("");
        rua.setText("");
        numero.setText("");
        bairro.setText("");
        complemento.setText("");
        cidade.setText("");
        estado.setText("");
        cep.setText("");
        info.clear();
        total = 0;
        totalFinal = 0;
        taxaEntrega.setText("");
        valPedido.setText("");
        valTotal.setText("");
        painel.setText("");
    }//GEN-LAST:event_confirmarActionPerformed

