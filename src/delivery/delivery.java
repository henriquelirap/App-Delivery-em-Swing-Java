    private void salvarpizzaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarpizzaActionPerformed
        int selecao = tamPizza.getSelectedIndex();
        if (selecao == 0) {
            pizza = null;
            tamanhopizza = null;
            precoPizza = null;
        }
        
        if (selecao != 0 && pizza != null) {
            if (selecao == 1) {
                total += 34;
            }
            if (selecao == 2) {
                total += 38;
            }
            if (selecao == 3) {
                total += 44;
            }
            if (selecao == 4) {
                total += 52;
            }

            info.add("1 Pizza " + pizza + ", " + tamanhopizza + " - " + precoPizza + "\n");
            painel.setText(info.toString());

        }
        totalFinal = total;
        pizza = null;
        tamanhopizza = null;
        precoPizza = null;
        calabresap.setSelected(false);
        frangop.setSelected(false);
        atump.setSelected(false);
        queijop.setSelected(false);
        portp.setSelected(false);
        napop.setSelected(false);
        chocp.setSelected(false);
        romeup.setSelected(false);
        tamPizza.setSelectedIndex(0);
        valPedido.setText(Double.toString(total));
        valTotal.setText(Double.toString(totalFinal));
    }//GEN-LAST:event_salvarpizzaActionPerformed

